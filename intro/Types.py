#   None Type
#The NoneType has only 1 single possible value: None
type(None)  #NoneType

#None is the default return value of a function
return_value = print('Hello world')
print(return_value)  #None

#   Boolean Type
#The Bool has only 2 possible values: True && False and they are case sensitives.
result = (4 < 5)
result  #True
type(result)    #bool